    private void squareButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_squareButtonActionPerformed
        if(isInteger(jTextPane1.getText()))
        {
            int square = Integer.parseInt(jTextPane1.getText());
            jTextPane1.setText("" + square * square);
        }
    }//GEN-LAST:event_squareButtonActionPerformed

    private void squareRootButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_squareRootButtonActionPerformed
        if(isInteger(jTextPane1.getText()))
        {
            int square = Integer.parseInt(jTextPane1.getText());
            square = (int) Math.sqrt(square);
            calcScreen = "" + square;
            jTextPane1.setText(calcScreen);
        }
    }//GEN-LAST:event_squareRootButtonActionPerformed

    private void negateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_negateButtonActionPerformed
        if(isInteger(jTextPane1.getText()))
        {
            int negate = Integer.parseInt(jTextPane1.getText());
            calcScreen = "" + (negate * -1);
            jTextPane1.setText(calcScreen);
        }
    }//GEN-LAST:event_negateButtonActionPerformed

    private Boolean isInteger(String text)
    {
        if(text.isEmpty())
        {
            return false;
        }

        try
        {
            int textText = Integer.parseInt(text);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    
