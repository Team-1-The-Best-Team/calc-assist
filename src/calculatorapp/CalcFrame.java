    private void equalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalButtonActionPerformed
        ScriptEngineManager se = new ScriptEngineManager();
        ScriptEngine equal = se.getEngineByName("JavaScript");
        try {
            outputTotal = (int)equal.eval(calcScreen);
        } catch (ScriptException ex) {
            Logger.getLogger(CalcFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        calcScreen = "" + outputTotal;
        jTextPane1.setText(calcScreen);
    }//GEN-LAST:event_equalButtonActionPerformed

